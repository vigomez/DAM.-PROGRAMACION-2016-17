
public class Variables {

	public static void main(String[] args) {
		// Definimos diferentes tipos de variables y las inicializamos
		int numero1=25; //entero
		int numero2=-26; //entero negativo
		float numero_dec=12.2f; //decimal, atención a la f
		double numero_declargo=12.2; //decimal con más memoria asignada
		char letra='a'; //carácter, entre comillas simples
		String frase="Estamos probando"; //String, entre comillas dobles
		Boolean afirmacion=true; //Booleana, true o false
		
		//Imprimimos por terminal
		System.out.println("Numero entero positivo: "+numero1);
		System.out.println("Numero entero negativo; "+numero2);
		System.out.println("Numero decimal float: "+numero_dec);
		System.out.println("Numero decimal double: "+numero_declargo);
		System.out.println("Letra: "+letra);
		System.out.println("String: "+frase);
		System.out.println("Booleano: "+afirmacion);	

	}

}
